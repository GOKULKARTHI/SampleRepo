package org.six;

public class EmpDetail extends DemoSix {
	public EmpDetail(int empId, String name, long phone, String address, String dob, String doj, String eMail,
			String gender, double sal) {
		this.empId = empId;
		this.name = name;
		this.phone = phone;
		this.address = address;
		this.dob = dob;
		this.doj = doj;
		this.email = eMail;
		this.gender = gender;
		this.salary = sal;
	}
}
